/* 17. Count Total Elements in Vector
Problem Statement:
Find total number of elements in Vector.
Input:
[5,10,15,20]

Output:
Size = 4
Explanation:
Use vector.size().
 */
 
import java.util.*;
public class q17CountElement
{
 public static void main(String x[])
 {
	 Vector<Integer> v=new Vector<>();
	 v.add(5);
	 v.add(10);
	  v.add(15);
	  v.add(20);
	  
	  
	   
	   System.out.println("Size--->"+v.size());
   
  }
 
}